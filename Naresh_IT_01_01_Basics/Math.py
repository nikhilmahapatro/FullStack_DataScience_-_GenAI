# Math is a module, it has multiple functions

import math as m

# Square root
print(m.sqrt(25))
print(m.sqrt(42))

# Floor & Ceil
print(m.floor(12.3))        # least value
print(m.ceil(12.3))         # highest value

# Power
print(m.pow(3,2))     # 3 power 2 = 9
print(m.pow(5,3))     # 5 power 3 = 125

# Constants
print(m.pi)
print(m.e)